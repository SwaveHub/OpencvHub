/**
 * @brief  cv画图函数
 * @param  无
 * @retval 状态码
 */
int cv_draw(void)
{
	Mat dispImg = Mat::zeros(500, 500, CV_8UC3);

	circle(dispImg, Point(100, 100), 50, CV_RGB(255, 0, 0), 1, 8, 0); /* Circle */
	line(dispImg, Point(0, 150), Point(150, 0), CV_RGB(0, 255, 0), 1, 8, 0); /* Line */
	rectangle(dispImg, Rect(0, 0, 150, 150), CV_RGB(0, 0, 255), 1, 8, 0); /* Rect */

	imshow("dispImg", dispImg);
	/* 等待用户按键 */
	waitKey(0);

	return 0;
}

